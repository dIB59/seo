//! Extension Result Types
//!
//! This module defines the result types returned by extensions during execution.
//! Each capability has its own result type with relevant data.

use std::collections::HashMap;
use serde::{Deserialize, Serialize};

use crate::contexts::{IssueSeverity, NewIssue};

/// Result from the data extraction phase.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExtractionResult {
    /// The extension ID that produced this result
    pub extension_id: String,
    
    /// Extracted data as key-value pairs
    pub data: HashMap<String, ExtractedValue>,
    
    /// Metadata about the extraction
    pub metadata: ExtractionMetadata,
}

/// A single extracted value with type information.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(untagged)]
pub enum ExtractedValue {
    /// Single text value
    Text(String),
    
    /// Numeric value
    Number(f64),
    
    /// Boolean value
    Boolean(bool),
    
    /// JSON value for complex data
    Json(serde_json::Value),
    
    /// List of strings
    List(Vec<String>),
    
    /// Key-value map
    Map(HashMap<String, String>),
    
    /// Table of data (list of maps)
    Table(Vec<HashMap<String, String>>),
}

impl ExtractedValue {
    /// Convert to a JSON value for serialization
    pub fn to_json(&self) -> serde_json::Value {
        match self {
            Self::Text(s) => serde_json::Value::String(s.clone()),
            Self::Number(n) => serde_json::Value::Number(
                serde_json::Number::from_f64(*n).unwrap_or_else(|| serde_json::Number::from(0))
            ),
            Self::Boolean(b) => serde_json::Value::Bool(*b),
            Self::Json(v) => v.clone(),
            Self::List(v) => serde_json::to_value(v).unwrap_or(serde_json::Value::Null),
            Self::Map(m) => serde_json::to_value(m).unwrap_or(serde_json::Value::Null),
            Self::Table(t) => serde_json::to_value(t).unwrap_or(serde_json::Value::Null),
        }
    }
    
    /// Try to get as text
    pub fn as_text(&self) -> Option<&str> {
        match self {
            Self::Text(s) => Some(s),
            _ => None,
        }
    }
    
    /// Try to get as number
    pub fn as_number(&self) -> Option<f64> {
        match self {
            Self::Number(n) => Some(*n),
            _ => None,
        }
    }
    
    /// Try to get as list
    pub fn as_list(&self) -> Option<&[String]> {
        match self {
            Self::List(v) => Some(v),
            _ => None,
        }
    }
}

impl Default for ExtractedValue {
    fn default() -> Self {
        Self::Text(String::new())
    }
}

/// Metadata about an extraction operation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExtractionMetadata {
    /// Time taken for extraction in milliseconds
    pub duration_ms: u64,
    
    /// Number of items extracted
    pub items_count: usize,
    
    /// Whether the extraction was successful
    pub success: bool,
    
    /// Error message if extraction failed
    pub error: Option<String>,
    
    /// Additional metadata
    pub extra: HashMap<String, String>,
}

impl Default for ExtractionMetadata {
    fn default() -> Self {
        Self {
            duration_ms: 0,
            items_count: 0,
            success: true,
            error: None,
            extra: HashMap::new(),
        }
    }
}

impl ExtractionResult {
    /// Create a new extraction result
    pub fn new(extension_id: String) -> Self {
        Self {
            extension_id,
            data: HashMap::new(),
            metadata: ExtractionMetadata::default(),
        }
    }
    
    /// Add a value to the result
    pub fn with_value(mut self, key: String, value: ExtractedValue) -> Self {
        self.data.insert(key, value);
        self.metadata.items_count = self.data.len();
        self
    }
    
    /// Set the duration
    pub fn with_duration(mut self, ms: u64) -> Self {
        self.metadata.duration_ms = ms;
        self
    }
    
    /// Mark as failed with error
    pub fn with_error(mut self, error: String) -> Self {
        self.metadata.success = false;
        self.metadata.error = Some(error);
        self
    }
    
    /// Convert all data to a JSON map
    pub fn to_json_map(&self) -> HashMap<String, serde_json::Value> {
        self.data
            .iter()
            .map(|(k, v)| (k.clone(), v.to_json()))
            .collect()
    }
}

/// Result from the validation/issue generation phase.
#[derive(Debug, Clone)]
pub struct ValidationResult {
    /// The extension ID that produced this result
    pub extension_id: String,
    
    /// Issues generated by this extension
    pub issues: Vec<NewIssue>,
    
    /// Score impact (negative for issues, 0 for no impact)
    pub score_impact: f64,
    
    /// Whether the validation passed (no critical issues)
    pub passed: bool,
    
    /// Metadata about the validation
    pub metadata: ValidationMetadata,
}

/// Metadata about a validation operation.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ValidationMetadata {
    /// Time taken for validation in milliseconds
    pub duration_ms: u64,
    
    /// Number of issues by severity
    pub issues_by_severity: HashMap<String, usize>,
    
    /// Additional metadata
    pub extra: HashMap<String, String>,
}

impl Default for ValidationMetadata {
    fn default() -> Self {
        Self {
            duration_ms: 0,
            issues_by_severity: HashMap::new(),
            extra: HashMap::new(),
        }
    }
}

impl ValidationResult {
    /// Create a new validation result
    pub fn new(extension_id: String) -> Self {
        Self {
            extension_id,
            issues: Vec::new(),
            score_impact: 0.0,
            passed: true,
            metadata: ValidationMetadata::default(),
        }
    }
    
    /// Add an issue to the result
    pub fn with_issue(mut self, issue: NewIssue) -> Self {
        // Update severity counts
        let severity_key = format!("{:?}", issue.severity).to_lowercase();
        *self.metadata.issues_by_severity.entry(severity_key).or_insert(0) += 1;
        
        // Update score impact based on severity
        self.score_impact -= match issue.severity {
            IssueSeverity::Critical => 10.0,
            IssueSeverity::Warning => 5.0,
            IssueSeverity::Info => 1.0,
        };
        
        // Check if still passing (no critical issues)
        if matches!(issue.severity, IssueSeverity::Critical) {
            self.passed = false;
        }
        
        self.issues.push(issue);
        self
    }
    
    /// Add multiple issues
    pub fn with_issues(mut self, issues: Vec<NewIssue>) -> Self {
        for issue in issues {
            self = self.with_issue(issue);
        }
        self
    }
    
    /// Set the duration
    pub fn with_duration(mut self, ms: u64) -> Self {
        self.metadata.duration_ms = ms;
        self
    }
    
    /// Check if there are any issues
    pub fn has_issues(&self) -> bool {
        !self.issues.is_empty()
    }
}

/// Result from the data export phase.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ExportResult {
    /// The extension ID that produced this result
    pub extension_id: String,
    
    /// Whether the export was successful
    pub success: bool,
    
    /// The exported data (for API responses)
    pub data: Option<serde_json::Value>,
    
    /// Target endpoint that was used
    pub endpoint: Option<String>,
    
    /// HTTP status code (for webhook exports)
    pub status_code: Option<u16>,
    
    /// Error message if export failed
    pub error: Option<String>,
    
    /// Time taken for export in milliseconds
    pub duration_ms: u64,
}

impl ExportResult {
    /// Create a successful export result
    pub fn success(extension_id: String, data: serde_json::Value) -> Self {
        Self {
            extension_id,
            success: true,
            data: Some(data),
            endpoint: None,
            status_code: None,
            error: None,
            duration_ms: 0,
        }
    }
    
    /// Create a failed export result
    pub fn failure(extension_id: String, error: String) -> Self {
        Self {
            extension_id,
            success: false,
            data: None,
            endpoint: None,
            status_code: None,
            error: Some(error),
            duration_ms: 0,
        }
    }
    
    /// Set the endpoint
    pub fn with_endpoint(mut self, endpoint: String) -> Self {
        self.endpoint = Some(endpoint);
        self
    }
    
    /// Set the status code
    pub fn with_status(mut self, code: u16) -> Self {
        self.status_code = Some(code);
        self
    }
    
    /// Set the duration
    pub fn with_duration(mut self, ms: u64) -> Self {
        self.duration_ms = ms;
        self
    }
}

/// Combined result from the full pipeline execution.
#[derive(Debug, Clone, Default)]
pub struct PipelineResult {
    /// Page ID that was processed
    pub page_id: String,
    
    /// URL that was processed
    pub url: String,
    
    /// All extraction results
    pub extraction_results: Vec<ExtractionResult>,
    
    /// All validation results
    pub validation_results: Vec<ValidationResult>,
    
    /// All export results
    pub export_results: Vec<ExportResult>,
    
    /// Combined extracted data
    pub extracted_data: HashMap<String, serde_json::Value>,
    
    /// All issues generated
    pub issues: Vec<NewIssue>,
    
    /// Total processing time in milliseconds
    pub total_duration_ms: u64,
}

impl PipelineResult {
    /// Create a new pipeline result
    pub fn new(page_id: String, url: String) -> Self {
        Self {
            page_id,
            url,
            ..Default::default()
        }
    }
    
    /// Add extraction results
    pub fn add_extraction_results(&mut self, results: Vec<ExtractionResult>) {
        for result in &results {
            // Merge extracted data into combined map
            for (key, value) in &result.data {
                self.extracted_data.insert(key.clone(), value.to_json());
            }
        }
        self.extraction_results = results;
    }
    
    /// Add validation results
    pub fn add_validation_results(&mut self, results: Vec<ValidationResult>) {
        for result in &results {
            self.issues.extend(result.issues.clone());
        }
        self.validation_results = results;
    }
    
    /// Add export results
    pub fn add_export_results(&mut self, results: Vec<ExportResult>) {
        self.export_results = results;
    }
    
    /// Check if any extractions failed
    pub fn has_extraction_errors(&self) -> bool {
        self.extraction_results.iter().any(|r| !r.metadata.success)
    }
    
    /// Check if any exports failed
    pub fn has_export_errors(&self) -> bool {
        self.export_results.iter().any(|r| !r.success)
    }
    
    /// Get total issue count
    pub fn total_issues(&self) -> usize {
        self.issues.len()
    }
    
    /// Get issues by severity
    pub fn issues_by_severity(&self, severity: IssueSeverity) -> Vec<&NewIssue> {
        self.issues
            .iter()
            .filter(|i| i.severity == severity)
            .collect()
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    
    #[test]
    fn test_extracted_value() {
        let text = ExtractedValue::Text("hello".to_string());
        assert_eq!(text.as_text(), Some("hello"));
        
        let num = ExtractedValue::Number(42.5);
        assert_eq!(num.as_number(), Some(42.5));
        
        let list = ExtractedValue::List(vec!["a".to_string(), "b".to_string()]);
        assert_eq!(list.as_list(), Some(&["a".to_string(), "b".to_string()][..]));
    }
    
    #[test]
    fn test_extraction_result() {
        let result = ExtractionResult::new("ext-1".to_string())
            .with_value("title".to_string(), ExtractedValue::Text("Test".to_string()))
            .with_duration(10);
        
        assert_eq!(result.extension_id, "ext-1");
        assert_eq!(result.data.len(), 1);
        assert_eq!(result.metadata.duration_ms, 10);
    }
    
    #[test]
    fn test_validation_result() {
        let result = ValidationResult::new("rule-1".to_string())
            .with_issue(NewIssue {
                job_id: "job-1".to_string(),
                page_id: Some("page-1".to_string()),
                issue_type: "test".to_string(),
                severity: IssueSeverity::Warning,
                message: "Test issue".to_string(),
                details: None,
            });
        
        assert!(!result.issues.is_empty());
        assert!(result.passed); // Warning doesn't fail
        assert!(result.score_impact < 0.0);
    }
    
    #[test]
    fn test_export_result() {
        let success = ExportResult::success("ext-1".to_string(), serde_json::json!({"key": "value"}));
        assert!(success.success);
        assert!(success.data.is_some());
        
        let failure = ExportResult::failure("ext-1".to_string(), "Connection failed".to_string());
        assert!(!failure.success);
        assert!(failure.error.is_some());
    }
}
